#include <iostream>

using namespace std;

int main()
{
    int m, n;

    cout << "Enter value : ";
    cin >> m;
    cout << "Enter value : ";
    cin >> n;

    cout << "Addition of Value : " << m + n << endl;
    cout << "Subtraction of value : " << m - n << endl;
    cout << "Multiplication of value : " << m * n << endl;
    cout << "Division of value : " << m / n;

    return 0;
}